const emojiWeKnow = Object.keys(emojiMeanings);

export default function App() {
  const [meaning, setMeaning] = useState("");

  function onChangeHandler(event) {
    let userInput = event.target.value;
    let meaning = emojiMeanings[userInput];

    if (meaning === undefined) {
      setMeaning("Failure to read your emoji");
    } else {
      setMeaning(meaning);
    }
  }

  function onClickHandler(item) {
    let meaning = emojiMeanings[item];
    setMeaning(meaning);
  }

  return (
    <div className="App">
      <h1>EMOZY-PEDIA</h1>
      <input
        className="user-input"
        placeholder="Put an emozy for meaning"
        onChange={onChangeHandler}
      />
      <div className="meaning-output">{meaning}</div>

      <div className="emozies-div">
        {emojiWeKnow.map(function (item) {
          return (
            <span className="emozies" onClick={() => onClickHandler(item)}>
              {item}
            </span>
          );
        })}
      </div>
    </div>
  );
}
